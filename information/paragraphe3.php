      <h1>Bandages</h1>
	  <td  colspan="3" rowspan="2"  >
  <img src="mes_img/Capture.PNG" alt="Header image" width="500px" height="300px" align="center" nowrap="nowrap"/>
 </td>
      <p>
	Les bandages sont une technique thérapeutique utilisée par de nombreux professionnels de santé. Elle consiste à appliquer localement 
  plusieurs couches de bandes de tissus pour immobiliser une articulation, maintenir un pansement ou exercer une compression sur une partie du corps.<br>
  Les bandages peuvent notamment s'avérer utiles pour laisser un temps d'immobilisation et de guérison aux entorses et fractures légères, maintenir des pansements de brûlures,
  maintenir réduite une hernie... Ils sont généralement utilisés en complément d'une pommade, ou toute autre substance appliquée localement avant la mise de la bande.

	
</p>
