      <h1>Expulsion d'un corps étranger</h1>
	  <td  colspan="3" rowspan="2"  >
  <img src="mes_img/corps etrangére.jpg" alt="Header image" width="500px" height="300px" align="center" nowrap="nowrap"/>
 </td>
      <p>
		Résumé :<br>
Les corps étrangers (CE) trachéo-bronchiques ont une gravité potentielle, qu'il importe de ne pas sous-estimer.<br>
 En effet 5 à 10 enfants en décèdent chaque année dans notre pays. Hormis quelques cas exceptionnels d'asphyxie immédiate, lorsque le corps étranger 
 se bloque en travers des cordes vocales, la symptomatologie habituelle se résume à la survenue brutale d'un syndrome de pénétration,
 chez un jeune enfant de 1 à 3 ans, jusque-là bien portant. Cet accident est beaucoup plus rare chez le grand enfant et l’adulte.

</p>
