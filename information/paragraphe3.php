  <div id="menu23" class="tab-pane fade"style="margin-left:350px;float:right;margin-top:-500px;">
      <h1>Massage cardiaque</h1>
	  <td  colspan="3" rowspan="2"  >
  <img src="mes_img/massage cardiaque.jpg" alt="Header image" width="500px" height="300px" align="center" nowrap="nowrap"/>
 </td>
      <p>
	Normalement, le cœur, qui est situé dans la cage thoracique, se contracte régulièrement et permet ainsi la circulation du sang dans l’ensemble de l’organisme.
En fait, le sang est chassé du cœur vers le reste du corps à chaque contraction cardiaque.<br>
Lorsque le coeur ne fonctionne plus, on peut maintenir cette éjection du sang du cœur vers les autres organes grâce à un massage cardiaque
 externe : en appuyant fortement sur le sternum (partie du thorax située entre les côtes), on comprime les cavités du cœur, ce qui en expulse le sang.<br>
 Lorsqu’on relâche la pression sur le sternum, les cavités du cœur se remplissent à nouveau. En alternant ainsi pression et relâchement,
 on obtient une circulation sanguine qui, bien que moins puissante que la naturelle, permet de maintenir l’irrigation sanguine de l’organisme.
	
</p>
  </div>
  
  <div id="menu24" class="tab-pane fade"style="margin-left:350px;float:right;margin-top:-550px;">
      <h1>Position Latérale de Sécurité</h1>
	  <td  colspan="3" rowspan="2"  >
  <img src="mes_img/postion latérale de sécurité.jpg" alt="Header image" width="500px" height="300px" align="center" nowrap="nowrap"/>
 </td>
      <p>
	La position latérale de sécurité (ou PLS) est un geste de premiers secours à pratiquer systématiquement lorsque l'on est 
	en présence d'une personne inconsciente, qui respire normalement et qui est décubitus dorsal (couchée sur le dos).<br>
 Cette technique sert à maintenir la liberté des voies aériennes supérieures, c'est-à-dire le passage de l'air jusqu'aux poumons.<br>
 En effet, le risque majeur est que la personne s'étouffe par la fermeture de l'épiglotte (clapet qui empêche normalement les aliments 
 de pénétrer dans les poumons) et le contenu de l'estomac (qui se vide lentement, puisque le muscle qui le ferme n'a plus de tonus).<br>
 Pour ce faire, on place la victime sur le côté, en chien de fusil, en maintenant sa tête alignée dans l'axe du dos, la bouche ouverte ; 
 en langage médical, on parle parfois de décubitus latéral pour signifier que le corps est horizontal et tourné sur le côté.
	
</p>
  </div>
  
  <div id="menu25" class="tab-pane fade"style="margin-left:350px;float:right;margin-top:-550px;">
      <h1>Brulures</h1>
	  <td  colspan="3" rowspan="2"  >
  <img src="mes_img/brulure.PNG" alt="Header image" width="500px" height="300px" align="center" nowrap="nowrap"/>
 </td>
      <p>
	Tout d’abord, il est important de savoir s’il faut consulter en urgence un médecin ou non, choisir le point A. ou B. :<br>
	 A. Il faudra consulter en urgence un médecin ou professionnel des urgences (ex. pompiers) si la brûlure est du premier ou du deuxième 
	 degré et qu’elle se manifeste sur une surface de la peau supérieure à la surface de la main, qu’elle est située au niveau des articulations,
	 qu’elle touche un nourrisson ou un petit enfant, que les yeux et la bouche sont touchés ou qu’elle est du troisième degré (c’est-à-dire que toutes
	 les couches de la peau sont touchées, lire aussi ci-dessous sous Symptômes).<br>
	  B. Si la brûlure est du premier ou du deuxième degré sans les limitations énoncées sous A., vous pouvez vous soigner vous-même (automédication).
Il s’agira de refroidir la brûlure le plus vite possible avec de l’eau froide avec une température d’environ 15°C pendant 15 à 20 minutes. L’eau froide 
permet de refroidir les tissus intérieurs. En cas de brûlure, on continue à “brûler de l’intérieur”, d’où l’intérêt et la nécessité d’appliquer de l’eau 
froide à tiède (env. 15°C) sur une période relativement longue.<br>

</p>
