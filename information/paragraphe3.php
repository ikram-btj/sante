  <div id="menu21" class="tab-pane fade"style="margin-left:350px;float:right;margin-top:-500px;">
      <h1>Hémorragier externes</h1>
	  <td  colspan="3" rowspan="2"  >
  <img src="mes_img/Capture1.PNG" alt="Header image" width="500px" height="300px" align="center" nowrap="nowrap"/>
 </td>
      <p>
	Une hémorragie est un écoulement de sang hors des vaisseaux sanguins.<br>
Une hémorragie est dite externe quand cet écoulement de sang se produit à l’extérieur du corps (blessure, plaie).<br>
La gravité de l’hémorragie vient de ce que le volume de sang perdu menace le fonctionnement normal de l’organisme.<br>
	
</p>
  </div>
   
