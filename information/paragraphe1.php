      <h1>Diarrhé</h1>
      <p>La diarrhée est un problème fréquent. Elle se caractérise par des selles de consistance liquide ou molle, plus volumineuses et nombreuses qu’à l’habitude (plus de 3 selles par jour).

Il ne s’agit pas d’une maladie, mais d’un symptôme. Sa cause la plus fréquente est l’ingestion d’eau ou d’aliments contaminés. Elle dure 1 jour ou 2, puis disparaît sans nécessiter de traitement.

Pendant toute sa durée, il faut bien s’hydrater, car le corps peut perdre beaucoup d’eau et de sels minéraux par les selles. 
Cette mesure est particulièrement importante pour les enfants et les personnes âgées, pour lesquels la déshydratation peut avoir des conséquences plus sérieuses.
 En effet, après seulement 1 ou 2 jours de diarrhée, les jeunes enfants peuvent être déshydratés très gravement. 
 Dans les pays industrialisés, il est très rare qu’une diarrhée entraîne le décès. Néanmoins, dans les pays sous-développés, il s’agit de la 2e cause de mortalité chez les enfants de moins de 5 ans
	<h3>Causes</h3> 
	
 *Un manque d’absorption par les intestins du liquide contenu dans les selles ;<br>
 *Un transit intestinal accéléré, ce qui empêche l’assèchement des matières fécales ;<br>
 *Un passage anormal d’eau et de sels minéraux provenant du corps par la paroi des intestins.<br>
 *Une intoxication alimentaire (par exemple, de la volaille infectée par la salmonelle ou de la viande contaminée par la bactérie Escherichia coli) ;<br>
 *Une gastroentérite virale ;<br>
 *Le stress ou l’anxiété ;<br>
 *La prise d’antibiotiques : elle altère la flore intestinale, et réduit ainsi la capacité d’absorption de la paroi des intestins. Les antibiotiques causent la diarrhée chez 5 à 30 % des utilisateurs, selon le type d’antibiotique consommé ;<br>
 *Une maladie intestinale chronique : la maladie coeliaque (intolérance au gluten), la maladie de Crohn, le syndrome de l’intestin irritable, etc. ;<br>
 *Une intolérance au lactose ;<br>
 *L’hyperthyroïdie.<br>
 
 <h3>Quand consulter?</h3>
 •Une diarrhée très abondante (plus de 10 selles par jour) qui persiste plus de 48 heures ;<br>
 •Des signes de déshydratation, surtout chez un jeune enfant ou une personne âgée (voir plus bas) ;<br>
 •Une fièvre de 38,5 ºC (101,5 ºF) ou plus ;<br>
 •Du sang dans les selles ;<br>
 •De fortes douleurs abdominales ;<br>
 •Une diarrhée chronique.
	  </p>
