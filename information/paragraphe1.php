      <h1>Fièvre</h1>
      <p>
	  La fièvre est l'état d'un animal à sang chaud (homéotherme) dont la température interne est nettement supérieure (hyperthermie) à sa température ordinaire (37 °C en moyenne chez l'humain), de façon contrôlée.
	  On observe par la mesure à l'aide d'un thermomètre une élévation de la température corporelle habituelle. 
	  Ce phénomène physiologique semble être principalement une réponse hypothalamique stimulée par des substances pyrogènes principalement libérées par les macrophages et/ou lors des phénomènes inflammatoires
	  La fièvre semble être une réaction systémique des homéothermes à la présence de certains agents infectieux<br>
	  L'effet bénéfique est de perturber les réactions biochimiques (cf. constantes d'équilibres) des micro-organismes pathogènes afin de diminuer leur virulence.<br>
	Le phénomène se déroule suivant trois phases <br>
    •montée thermique<br>
    •plateau d’hyperthermie<br>
    •défervescence<br>
   Chez l'humain, la température corporelle normale moyenne est de 37 °C (entre 36,5 °C et 37,5 °C selon les individus). 
   La fièvre est définie par une température rectale au repos supérieure ou égale à 38,0 °C1. 
   Une fièvre au-delà de 40 °C est considérée comme un risque de santé majeur et immédiat, particulièrement chez les jeunes enfants (voir hyperthermie). 
   Lorsque la fièvre est modérée, entre 37,7 °C et 37,9 °C, on parle de fébricule.<br>
   <h3>Mesure de la température corporelle</h3>
   Il n'existe pas de définition précise universellement admise de la fièvre notamment du fait de difficultés concrètes de mesure en situation clinique (la température mesurée dépend du moment de la journée, de la proximité d'un repas, de caractéristiques environnementales). 
   Cependant le Brighton Collaboration Fever Working Group s'accorde à la définir en 2004 la fièvre comme relevant d'une température corporelle supérieure ou égale à 38 °C, et ce quelles que soient les modalités de mesure, l'âge ou les conditions environnementales. 
   L'OMS de son côté, considère comme fiévreuse une température axillaire supérieure ou égale à 37,5 °C
   <h3>Fièvre et risques de convulsions</h3>
   Chez le jeune enfant, cette fièvre peut entraîner des convulsions qui, si elles sont impressionnantes, sont en général bénignes ;
   il faut toutefois impérativement éviter que cette situation ne se prolonge, il faut donc abaisser lentement la température de l'ensemble du corps.
   <h3>Étiologie</h3>
   La fièvre est un signe médical fréquent.<br>
Il appartient au médecin d'essayer de la rattacher à une étiologie (diagnostic) et d'évaluer sa gravité.<br>
Pour établir un diagnostic devant ce signe le médecin recherchera ses caractéristiques sémiologiques : homme / femme - âge - antécédents - ethnie - facteurs de risques - caractère aiguë / prolongée - lieu de séjour - fièvre isolé ou regroupement syndromique - incidence et prévalence locales et saisonnières - etc.<br>
Malgré leurs faibles prévalences dans les pays occidentaux, il est indispensable que le médecin sache écarter des atteintes particulièrement graves :<br>
•au retour d'un voyage : paludisme<br>
•si fièvre persistante : tuberculose<br>
•associée à des douleurs articulaires : arthrite septique<br>
•isolée, chez l'homme : prostatite<br>
•en cas de souffle cardiaque : endocardite infectieuse<br>
  <h3>Traitement</h3>
  La fièvre ayant un rôle dans la lutte contre l'infection, pour un enfant n'étant pas sujet aux convulsions et hors urgence (voir ci-dessus), l'administration d'antipyrétique n'est plus systématique, et n'est envisagée qu'à partir de 38,5 °C.
  On conseille alors plutôt le paracétamol en monothérapie.
	</p>
