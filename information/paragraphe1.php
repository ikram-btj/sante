      <h1>Douleures dans la potrine</h1>
      <p>
 Les douleurs à la poitrine peuvent se manifester de diverses façons, qu’il s’agisse de points douloureux précis, de sensation de serrement ou de poids, de douleurs de type « coups de poignard », etc.
 Elles peuvent s’étendre du cou au sternum, être diffuses ou localisées.
 Ces douleurs peuvent avoir différentes origines mais doivent amener à consulter à rapidement. 
 Il peut en effet s’agir de douleurs avant-coureuses d’infarctus du myocarde (crise cardiaque), bien qu’il existe de nombreuses autres causes possibles.
	 <h3>Les causes</h3>
	 Ces douleurs peuvent être causées par :<br>

 *une crise cardiaque ou infarctus du myocarde : la douleur est intense, brutale et nécessite d’appeler les secours rapidement.
   ce qu’on appelle l’angine de poitrine ou angor, c’est-à-dire d’une insuffisance d’irrigation sanguine du cœur.<br>
 *Cette mauvaise irrigation est généralement due à une atteinte des artères coronaires, les vaisseaux qui apportent le sang au cœur (elles se bouchent). C’est une maladie chronique qui peut mener à l’infarctus. Environ 4% des adultes sont atteints de maladie coronarienne. La douleur est typiquement localisée derrière le sternum, déclenchée par l’effort.
   Elle peut irradier vers le cou, les mâchoires, les épaules ou les bras, des localisations qui sont parfois isolées.<br>
 *une dissection de l’aorte, qui correspond à l’entrée de sang à l'intérieur de la paroi de l'aorte<br>
 *une péricardite, c’est-à-dire une inflammation de l’enveloppe qui entoure le cœur, le péricarde, ou une myocardite, une inflammation du cœur lui-même<br>
 *une cardiomyopathie hypertrophique (maladie qui conduit la paroi du cœur à s’épaissir)<br>
	
  D’autres organes que le cœur peuvent être à l’origine de douleurs à la poitrine :<br>
 *causes pulmonaires : une pleurésie, une pneumonie, un abcès au poumon, une embolie pulmonaire, etc.<br>
 *causes digestives : reflux gastro-oesophagien (brûlures derrière le sternum), maladies de l’œsophage, ulcères gastriques, pancréatite…
 *douleurs musculaires ou osseuses (fracture de côte, par exemple)<br>
 *anxiété et crises de panique<br>
 <h3>Le traitement de la douleur thoracique</h3>
En dehors des situations d'urgence nécessitant une hospitalisation, prenez rendez-vous chez votre médecin traitant. 
Selon le type de douleur que vous présentez, il prescrira ou non des examens complémentaires.
 La douleur thoracique s'estompe grâce aux antalgiques et lors du traitement de la maladie en cause.
	  </p>
