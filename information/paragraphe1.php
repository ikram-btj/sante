      <h1>La constipation</h1>
      <p>La constipation (d'après le latin co- : « avec » + stipare : "rendre raide, compact") est une difficulté à déféquer.
	  Les selles sont alors généralement dures et de petit volume. Plusieurs définitions médicales coexistent, la plus courante est : « moins de trois selles par semaine et/ou difficultés à exonérer.<br></p>
	  <h3> Causes</h3>
	  <h3>Constipation primitive (ou constipation fonctionnelle)</h3>
	  <P> •Anomalie du contenu des selles : lorsqu'elles ne sont pas assez riches en eau ni en fibres en particulier. Ce type de constipation est courante chez les personnes dénutries : anorexie mentale, dépression, démence ainsi que lors de maladies graves et chroniques.<br>
•Diminution de la motricité digestive : liée à une insuffisance d'exercice physique, une prise excessive de laxatifs, un côlon irritable.<br>
•Prise de médicaments constipants : opioïdes, anticholinergiques, calcium, etc.<br>
•Plancher pelvien spastique (ou anisme) : liée à un excès du tonus sphinctérien, dont la décontraction n'est pas obtenue au cours de la défécation.<br>
•Causes psychogènes : on retrouve souvent un stress lié à la défécation, volontiers liée à un apprentissage problématique de la propreté, mais aussi maltraitance, abus sexuels dans l'enfance.<br>
•Stress du manque d'hygiène des toilettes, qui bloque la défécation.<br>
•La laxophobie (une crainte psychologique permanente d'avoir une diarrhée à un moment ou à un endroit dépourvu de toilettes), ce qui retarde ou stoppe la défécation.<br>
•Conditionnement social : les gens comparent souvent le séjour à la toilette à une perte de temps ; ils perturbent ainsi le réflexe en retardant l'exonération.<br>
•Flore intestinale : une diminution de la flore bifidogène et une augmentation de la flore méthanogène peut augmenter le risque de constipation1. La production de méthane semble entraîner un ralentissement du transit intestinal2.
 <h3>Constipation organique</h3>
  •Cancer du côlon ou du rectum<br>
•Sigmoïdite<br>
•Maladie de Hirschsprung<br>
•Mégacôlon, dolichocôlon<br>
•Problèmes métaboliques : diabète, hypothyroïdie, hypercalcémie, hypokaliémie (manque de potassium)...<br>
•Constipation « réflexe » à la douleur : fissure anale, hémorroïdes, abcès local, laxophobie, ...<br>
•Causes neurologiques : paraplégie, maladie de Parkinson, maladies de la moelle épinière (myélopathie)...
<h3>Orientation diagnostique</h3>
Le principal problème face à une constipation est de différencier constipation bénigne (fonctionnelle) et constipation liée à une maladie organique grave (en particulier le cancer colique).<br>
 Si une cause grave est suspectée, certains examens doivent être prescrits : coloscopie, abdomen sans préparation, lavement baryté, recherche de sang dans les selles, hémogramme... Ces examens n'ont aucune utilité s'il n'y a pas de signe d'appel et ne doivent pas être faits systématiquement3.
  <h3>Traitement</h3>
  Une hygiène de vie reste indispensable : aller à la selle à heure fixe et ne jamais se retenir, alimentation riche en fibres (légumes verts, céréales, son, fruits), bien s'hydrater (2 litres d'eau par jour, compris dans la prise de boisson et la prise alimentaire), exercice physique et relaxation.<br>
  Une rééducation par un kinésithérapeute spécialiste est nécessaire. La prescription de laxatifs non irritants (par voie orale ou rectale) peut aider quelques jours mais il faut tenter de s'en passer, les changements dans l'alimentation devant résoudre le problème5.<br>
  En effet, les laxatifs pris au long cours finissent par aggraver la constipation.
  </P>
