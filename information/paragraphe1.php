      <h1>Les Crampes</h1>
      <p>La crampe musculaire est une contraction soudaine, intense, involontaire et passagère d'une partie ou de la totalité d'un muscle. Sa durée est variable, généralement brève.</p>
	  <P> Les crampes peuvent survenir de façon inopinée ou être déclenchée par une contraction musculaire à l'occasion d'un effort comportant un raccourcissement musculaire.
La survenue de crampes musculaires est en général un phénomène bénin, survenant au repos (notamment la nuit, touchant fréquemment les muscles de la jambe) ou à l'effort.

De nombreux facteurs sont connus pour favoriser la survenue de crampes musculaires :
·La grossesse, la pilule ;
·Les états de déshydratation (coups de chaleur, sueurs profuses, vomissements, diarrhée) ;
·La dialyse de l' insuffisance rénale chronique (perte d'eau et de sels minéraux);
·L'abus d'excitants (café, thé), les toxicomanies ;
·Le froid.
<h3> Les signes de la maladie</h3>

<P>Lorsque la crampe survient, elle est généralement visible et palpable et cesse en général lors de l'élongation du muscle.

<h3>La consultation</h3>

Il est surtout utile pour retrouver des éléments en faveur d'une maladie sous-jacente responsable des crampes musculaires.

<h3>Examens et analyses complémentaires</h3>

Dans la majorité des cas, ils sont inutiles.

En revanche, des explorations biologiques ou radiologiques peuvent être utiles si le médecin soupçonne l'existence d'une maladie sous-jacente responsable des crampes musculaires.

<h3>Evolution de la maladie</h3>

Elles est bénigne dans la plupart des cas ou dépend de la maladie sous-jacente.</P>

<h2>Traitement</h2>
Dans la plupart des cas, aucun traitement n'est justifié : lorsque la crampe survient, il faut conseiller au patient d'étirer le muscle douloureux (par exemple, en cas de crampe du mollet, conseiller de tirer progressivement les orteils et le pied vers soi).<br> 
Parfois, une supplémentation en magnésium peut être utile, avec l'avis du médecin ou du pharmacien.
</P>
