      <h1>géne pour avaler</h1>
      <p>
 Avoir la sensation d'une gêne dans la gorge peut avoir plusieurs origines. 
 Il est important de savoir pourquoi l'on a cette sensation de gêne afin de pouvoir déterminer comment soigner le mal de gorge qui en découle.
 La gorge peut également être rouge et s'accompagner d'une toux sèche : bref, plusieurs autres symptômes qui peuvent accompagner cette gêne dans la gorge.
 Il est important de pouvoir décrire ce que l'on ressent et de prêter attention aux diverses sensations que l'on éprouve dans la journée.
	</h3>Causes possibles</h3>
 •Peut provenir d'un problème nerveux (stress, angoisse).<br>
 •Cela peut dans ce cas être psycho-somatique. On peut parfois lire que l'esprit agit sur le corps et inversement.<br>
 •Lorsque quelque chose « nous reste en travers de la gorge » ou lorsque l'on n'arrive pas à communiquer quelque chose d'important à quelqu'un, on peut ressentir une gêne dans la gorge qui va disparaitre dès le moment où la cause en sera résolue.<br>
 •Peut également être lié à un problème de la thyroïde. Dans le doute, demandez un bilan à votre médecin traitant.<br>
 •Peut venir d'un problème nasal qui se répercute sur la gorge et/ou les oreilles.<br>
	 <h3>Traitements à envisager</h3>
 •Dans tous les cas, la consultation d'un médecin traitant s'impose.<br>
 Il pourra ensuite vous orienter vers un ORL ou vous faire faire une prise de sang (par exemple pour un bilan de la thyroïde).
 •S'il n'y a rien de particulier et qu'il juge que le problème est d'origine psychosomatique ou nerveuse, il pourra vous orienter vers un psychothérapeute ou un sophrologue en cas de stress.<br>
	 
	  </p>
