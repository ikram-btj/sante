      <h1>L'amaigrissement</h1>
      <p>Un amaigrissement est une perte de poids. Il temoigne d'un desequilibre entre les apports et les depenses energetiques. 
	  Il peut etre volontaire, dans un but esthetique ou therapeutique, ou involontaire, pouvant alors reveler certaines maladies organiques evolutives, ou des troubles du comportement alimentaire.
	<h3>Étiologie</h3>
	Les principales causes d'amaigrissement sont1 :<br>
	•les troubles du comportement alimentaire : l'anorexie mentale et ses autres formes ;<br>
•l'amaigrissement avec alimentation conservée et hypercatabolisme : ◦affections endocriniennes ou métaboliques : hyperthyroïdie, diabète, hyperparathyroïdie primaire, phéochromocytome ;<br>
◦consommation importante de caféine et tabagisme ;<br>
◦affections digestives : malabsorption de la maladie cœliaque de l’adulte, de la maladie de Crohn, de l'entéropathie exsudative et de la pancréatite chronique ;<br>
◦amaigrissement paraphysiologique de la vieillesse.<br>

•l'amaigrissement d’accompagnement : cancers, maladies infectieuses, maladies neurologiques sévères, maladies hépatiques, cardiaques, rénales ou respiratoires sévères, autres maladies chroniques telles que maladies psychiatriques ou alcooliques ;<br>
•l'amaigrissement d'origine iatrogène, favorisé par la polymédication chez les personnes âgées ;<br>
•l'amaigrissement présumé isolé, principalement dû à des causes psychiques.

</p>
