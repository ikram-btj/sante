      <h1>Lumbago</h1>
	  <td  colspan="3" rowspan="2"  >
  <img src="mes_img/MAL.jpg" alt="Header image" width="500px" height="300px" align="center" nowrap="nowrap"/>
 </td><br>
      <p>
	Un lumbago est un terme désignant une lombalgie ou douleur au niveau des lombes, parties basses du dos. 
	60 à 70% des français ont souffert au moins une fois dans leur vie de lombalgies.<br> Plus de la moitié de la population française âgée de 30 à 64 ans
	a souffert de lombalgies au moins un jour dans les 12 derniers mois. La prévalence de la lombalgie de plus de 30 jours dans les 12 derniers mois est de 18% (Sources: Inserm).
	D'autre part, les lombalgies représentent la première cause de maladie professionnelle et d'arrêt du travail.<br> Elles représentent la 3ème cause consultation chez les hommes et la 6ème chez les femmes. 

	
</p>
