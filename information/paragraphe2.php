      <h1>Migraine</h1>
	  <td  colspan="3" rowspan="2"  >
  <img src="mes_img/mal-de-tete.jpg" alt="Header image" width="500px" height="300px" align="center" nowrap="nowrap"/>
 </td><br>
      <p>
 La migraine (du grec ancien ημικρανίον / êmikraníon, douleur touchant la « moitié du crâne ») est un type de céphalée (mal de tête) chronique fréquente, 
 invalidante, caractérisée par des maux de tête et des nausées. <br>La migraine est trois fois plus fréquente chez les femmes que chez les hommes1.
 Une migraine dure généralement entre 4 et 72 heures selon sa sévérité2 ; les symptômes incluent nausées, vomissements, photophobie (grande sensibilité à la lumière), 
 phonophobie (grande sensibilité au son) ; les symptômes s'aggravent généralement à cause des activités physiques3.<br> Approximativement un tiers des individus souffrant d'une migraine
 font l'expérience d'une aura juste avant la céphalée illusion visuelle ou autre trouble d'ordre sensoriel ou moteur4. On pense que les migraines sont causées par l'association de 
 facteurs environnementaux et de facteurs génétiques. Environ deux tiers des cas ont un contexte familial5.
 L’Organisation mondiale de la santé classe la maladie migraineuse au 20e rang des maladies ayant un impact sur le handicap et l’altération de la qualité de vie, et parmi les 10 premières maladies si l’on considère uniquement la population féminine
	
</p>
  </div>
  
