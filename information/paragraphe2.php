      <h1>Colique hépatique</h1>
	  <td  colspan="3" rowspan="2"  >
  <img src="mes_img/colique.jpg" alt="Header image" width="500px" height="300px" align="center" nowrap="nowrap"/>
 </td><br>
      <p>
 Une colique hépatique est un symptôme douloureux ressenti par un patient dans la partie supérieure droite de l'abdomen. <br>
 Cette douleur est généralement isolée ; elle ne s'accompagne pas de fièvre, d'altération de l'état général ou d'ictère.<br>
 On retrouve ce symptôme, par exemple, lors de expulsion d'une lithiase hors de la vésicule biliaire (en même temps que la bile), 
 lors de la contraction de celle-ci. La lithiase provoque une douleur vive en se déplaçant dans les voies biliaires : cette douleur peut irradier jusqu'aux côtes et l'épaule du côté droit.

</p>
  </div>
  
  <div id="menu15" class="tab-pane fade"style="margin-left:350px;float:right;margin-top:-680px;">
      <h1>Colique néphrétique</h1>
	  <td  colspan="3" rowspan="2"  >
  <img src="mes_img/CN.jpg" alt="Header image" width="500px" height="300px" align="center" nowrap="nowrap"/>
 </td><br>
      <p>
	La colique néphrétique désigne des douleurs paroxystiques, violentes, spontanées ou provoquées (secousses) de la fosse lombaire allant vers les organes génitaux. 
	Elle requiert une prise en charge immédiate. La douleur ressentie lors d'une colique néphrétique est réputée comme étant l'une des plus vives décrites en médecine.<br>
Cette douleur est le résultat d'une obstruction de la voie d'excrétion du haut appareil urinaire, le plus souvent consécutive à un calcul rénal (lithiase urinaire), entre le rein et la vessie. <br>
Bien souvent, le diagnostic est uniquement clinique parce que le patient a eu une hématurie et a éventuellement émis un calcul, ou parce qu'il est lithiasique connu et que ce n'est pas sa première crise.
Dans la majorité des cas ce sont des calculs à base de calcium (visibles à la radiographie), le plus commun étant celui composé d'oxalate de calcium (environ 50 % des cas).<br>
 Ensuite viennent les calculs d'acide urique (produit de dégradation des protéines). Enfin, de composition plus rare, sont les calculs de cystine (maladie héréditaire appelée cystinurie).
	
</p>
