      <h1>Angor</h1>
	  <td  colspan="3" rowspan="2"  >
  <img src="mes_img/Angina-Pectoris-400x533.jpg" alt="Header image" width="500px" height="300px" align="center" nowrap="nowrap"/>
 </td>
	  
      <p>
  L'angine de poitrine, ou angor, désigne une douleur thoracique survenant derrière le sternum. Il s'agit d'une douleur constrictive, 
  c'est-à-dire que le patient a l'impression que son thorax est pris dans un étau. Parfois la douleur est ressentie au niveau de l'estomac. <br>
  Cette douleur survient le plus souvent pendant un effort ou lors d'un stress et cède en cinq minutes environ au repos ou après la prise de trinitrine. 
  Les signes sont généralement évidents mais le diagnostic d'angor doit être confirmé par des examens complémentaires. L'angine de poitrine est un symptôme, témoin d'une maladie du cœur.<br>
	<h3>Les causes de l'angor </h3>
	Voyager lorsqu'on souffre d'une maladie cardiovasculaire Les compléments alimentaires contre les maladies cardiovasculaires 
	La phytothérapie pour prévenir les maladies cardiovasculaires Les médicaments de l'angor Les autres traitements de l'angor<br>
    L’angor et l’infarctus du myocarde sont des conséquences graves de l’athérosclérose et en ont les mêmes causes. 
	Cependant, toutes les crises d'angor ne sont pas nécessairement dues à une maladie coronarienne. <br>
	Certaines sont attribuables à des spasmes des artères coronaires qui entraînent une constriction suffisante pour diminuer le débit sanguin dans ces vaisseaux. 
	C'est l'angor de Prinzmetal dont l’origine est inconnue
</p>
