      <h1>Périarthrite de l'épaule</h1>
	  <td  colspan="3" rowspan="2"  >
  <img src="mes_img/epaule.jpg" alt="Header image" width="500px" height="300px" align="center" nowrap="nowrap"/>
 </td><br>
      <p>
 L'épaule douloureuse simple est la plus commune, en général secondaire à l'atteinte d'un tendon. 
 La douleur, unilatérale, d'intensité moyenne, apparaît souvent après 40 ans (mais les altérations peuvent commencer dès 20 ans), 
 spontanément ou après une chute sur l'épaule, un effort anormal, une utilisation intensive de l'articulation.<br> 
 La douleur est mécanique, c'est-à-dire accrue par le mouvement et diminuée par le repos mais peut augmenter la nuit si le patient se couche sur le côté douloureux.
 En fonction des symptômes, il s'agit d'une tendinite du sus-épineux, du long-biceps ou d'une calcification du tendon du sus-épineux.
 L'amplitude articulaire est normale. Seuls les mouvements contrariés sont douloureux.<br>
 La radiographie de l'épaule est le plus souvent normale mais dans quelques cas, elle peut montrer des calcifications au niveau du tendon atteint.
	
</p>
