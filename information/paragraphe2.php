      <h1>Appendicite</h1>
	  <td  colspan="3" rowspan="2"  >
  <img src="mes_img/appendice.jpg" alt="Header image" width="500px" height="300px" align="center" nowrap="nowrap"/>
 </td>
	  
      <p>L'appendicite est une inflammation aiguë de l'appendice, un segment d'intestin d'une dizaine de centimètres situé au niveau de la première partie du gros intestin.
   Elle provoque des douleurs abdominales, de la fièvre ou des troubles digestifs. Le traitement repose le plus souvent sur la résection de l'appendice infecté.<br>
   C'est l'inflammation aiguë de l'appendice, qui est un diverticule naturel qui prolonge le cæcum (entre l'intestin grêle et le colon droit).
   Le diagnostic de l'appendicite impose une résection chirurgicale rapide car il n'y a pas de parallélisme entre les signes cliniques et l'importance des lésions anatomiques.<br>
  Le pic de fréquence se situe entre 15 et 30 ans bien que l'appendicite puisse se voir à tout âge.
	
</p>
