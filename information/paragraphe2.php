      <h1>Sinusite</h1>
      <td  colspan="3" rowspan="2"  >
  <img src="mes_img/sinusite.jpg" alt="Header image" width="500px" height="300px" align="center" nowrap="nowrap"/>
 </td><br>
	  <p>
	La sinusite est une inflammation ou une infection d'un ou de plusieurs des quatre sinus (maxillaire, frontal, sphénoïdal ou ethmoïdal) que comporte le corps humain.<br>
 Il existe deux sortes de sinusites : la sinusite aiguë, de courte durée, qui peut être due à un rhume, à une allergie ou à des polluants de l'environnement, et une sinusite chronique qui dure plus de 12 semaines.
	</p>
